package day03EscapeSequences;
/*
Escape sequences: MUST be given eith double quote
\n: starts a new line
\t: horizontal tab
\\: backslash
\"
 */
public class EscapeSequences {


    public static void main(String[] args) {


        System.out.println("Java \nPython \nC#");

        System.out.println("-------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to you all today \nWhat class do we have next week?");

        System.out.println("-----------------------");

        System.out.println("\tJava is Cool Programming Language");

        System.out.println("---------------------");

        System.out.println("/ \\");   // backslash here you can see and need at 2 time \\

        System.out.println("-----------------------");

        System.out.println("My Favorite TV Show is \"Titans\""); // in order to print double code we need to use \"



    }





}
